first_number = int(input("Enter the first number: "))

second_number = int(input("Enter the second number: "))

square1 = first_number ** 2
square2 = second_number ** 2

print("The square of", first_number,"is", square1)
print("The square of", second_number,"is", square2)
print("The sum of the squares is", square1+square2)
print("The difference of the squares is", square1-square2)